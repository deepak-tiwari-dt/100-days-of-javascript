// Function declaration
function addition(num) {
  return num + 1; // adds 1
}

// Arrow function (block body)
const addition = (num) => {
  return num + 1; // explicit return
}

// Arrow function (concise)
const addition = (num) => num + 1; // implicit return

// Function expression
const addition = function(num) {
  return num + 1; // adds 1
}

// Arrow function (shortest form)
const addition = num => num + 1; // no () for single param